# =============================================================================
# Prerequisites
# =============================================================================

def is_masscan_installed() -> bool:
    """Check if masscan binary is available."""
    return shutil.which("masscan") is not None


def _is_mock_hostname(hostname: str, ip: str) -> bool:
    """
    Detect mock hostnames generated by run_ip_recon for IPs without PTR records.
    e.g. "10-0-0-1" for IP "10.0.0.1", "2001-db8--1" for "2001:db8::1"
    """
    expected_mock = ip.replace('.', '-').replace(':', '-')
    return hostname == expected_mock


