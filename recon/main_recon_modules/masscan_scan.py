"""
RedAmon - Masscan Port Scanner Module

High-speed SYN port scanning for large networks and IP ranges.
Runs as a native binary (built into the recon container) for simplicity.

Features:
- Fastest port scanner — optimized for subnet/CIDR scanning
- SYN scan only (no CONNECT fallback)
- Banner grabbing support
- NDJSON output for reliable parsing
- Normalized output compatible with Naabu's port_scan structure
"""

import json
import subprocess
import shutil
import tempfile
from pathlib import Path
from datetime import datetime
from typing import Dict, List, Set, Tuple
import sys

PROJECT_ROOT = Path(__file__).parent.parent
sys.path.insert(0, str(PROJECT_ROOT))

