def run_masscan_scan(recon_data: dict, output_file: Path = None, settings: dict = None) -> dict:
    """
    Run Masscan port scan on targets from recon data.

    Args:
        recon_data: Dictionary containing DNS/subdomain data
        output_file: Path to save enriched results (optional)
        settings: Settings dictionary from main.py

    Returns:
        Enriched recon_data with "masscan_scan" section added
    """
    print("\n" + "=" * 60)
    print("[*][Masscan] PORT SCANNER")
    print("=" * 60)

    if settings is None:
        settings = {}

    if not settings.get('MASSCAN_ENABLED', True):
        print("[-][Masscan] Disabled — skipping")
        return recon_data

    from recon.helpers import print_effective_settings
    print_effective_settings(
        "Masscan",
        settings,
        keys=[
            ("MASSCAN_ENABLED", "Toggle"),
            ("MASSCAN_TOP_PORTS", "Ports"),
            ("MASSCAN_CUSTOM_PORTS", "Ports"),
            ("MASSCAN_RATE", "Performance"),
            ("MASSCAN_WAIT", "Performance"),
            ("MASSCAN_RETRIES", "Performance"),
            ("MASSCAN_BANNERS", "Features"),
            ("MASSCAN_EXCLUDE_TARGETS", "Features"),
            ("USE_TOR_FOR_RECON", "Anonymity"),
        ],
    )

    MASSCAN_RATE = settings.get('MASSCAN_RATE', 1000)
    MASSCAN_CUSTOM_PORTS = settings.get('MASSCAN_CUSTOM_PORTS', '')
    MASSCAN_TOP_PORTS = settings.get('MASSCAN_TOP_PORTS', '1000')
    MASSCAN_BANNERS = settings.get('MASSCAN_BANNERS', False)
    USE_TOR = settings.get('USE_TOR_FOR_RECON', False)

    # Masscan uses raw SYN packets — incompatible with Tor proxy
    if USE_TOR:
        print("[!][Masscan] Cannot use Tor proxy (raw SYN packets bypass TCP stack) — skipping")
        return recon_data

    if not is_masscan_installed():
        print("[!][Masscan] Binary not found. Ensure masscan is installed.")
        return recon_data

    # Extract targets — masscan needs IPs, not hostnames
    print("[*][Masscan] Extracting IP targets from recon data...")

    # In IP mode, use expanded IPs directly (may include CIDRs)
    metadata = recon_data.get("metadata", {})
    if metadata.get("ip_mode"):
        all_targets = metadata.get("expanded_ips", [])
        raw_map = metadata.get("ip_to_hostname", {})
        # Normalize: main.py stores {ip: hostname_str}, we need {ip: [hostname_str]}.
        # Mock hostnames (e.g. "10-0-0-1" for IPs without PTR) must be excluded — they're
        # not routable and would produce invalid URLs in http_probe. For IPs without
        # real PTR records, use the IP itself as the hostname (matching Naabu's behavior).
        ip_to_hostnames = {}
        for ip, val in raw_map.items():
            if isinstance(val, list):
                real = [h for h in val if not _is_mock_hostname(h, ip)]
                ip_to_hostnames[ip] = real if real else [ip]
            elif isinstance(val, str) and val and not _is_mock_hostname(val, ip):
                ip_to_hostnames[ip] = [val]
            else:
                ip_to_hostnames[ip] = [ip]
    else:
        all_targets, ip_to_hostnames = resolve_targets_to_ips(recon_data)

    if not all_targets:
        print("[!][Masscan] No IP targets found in recon data")
        return recon_data

    print(f"[*][Masscan] Total IP targets: {len(all_targets)}")

    scan_temp_dir = Path(tempfile.mkdtemp(prefix="redamon_masscan_"))

    try:
        # Write targets
        targets_file = scan_temp_dir / "targets.txt"
        with open(targets_file, 'w') as f:
            for target in all_targets:
                f.write(f"{target}\n")

        masscan_output = scan_temp_dir / "masscan_output.ndjson"

        cmd = build_masscan_command(str(targets_file), str(masscan_output), settings)

        print(f"[*][Masscan] Starting scan...")
        print(f"[*][Masscan] Ports: {MASSCAN_CUSTOM_PORTS if MASSCAN_CUSTOM_PORTS else f'top {MASSCAN_TOP_PORTS}'}")
        print(f"[*][Masscan] Rate: {MASSCAN_RATE} pps")
        print(f"[*][Masscan] Wait: {settings.get('MASSCAN_WAIT', 10)}s")
        print(f"[*][Masscan] Retries: {settings.get('MASSCAN_RETRIES', 1)}")
        print(f"[*][Masscan] Banners: {MASSCAN_BANNERS}")

        start_time = datetime.now()

        process = subprocess.Popen(
            cmd,
            stdout=subprocess.PIPE,
            stderr=subprocess.PIPE,
            text=True,
        )

        _, stderr = process.communicate(timeout=1800)

        end_time = datetime.now()
        duration = (end_time - start_time).total_seconds()

        if process.returncode != 0 and not masscan_output.exists():
            if "permission" in (stderr or "").lower() or "raw socket" in (stderr or "").lower():
                print(f"[!][Masscan] Permission denied — masscan requires root or CAP_NET_RAW")
            else:
                print(f"[!][Masscan] Scan failed: {(stderr or '')[:200]}")
            return recon_data

        # Parse results
        print(f"[*][Masscan] Parsing results...")
        results = parse_masscan_output(str(masscan_output), ip_to_hostnames)

        masscan_results = {
            "scan_metadata": {
                "scanner": "masscan",
                "scan_timestamp": start_time.isoformat(),
                "scan_duration_seconds": round(duration, 2),
                "scan_type": "syn",
                "ports_config": MASSCAN_CUSTOM_PORTS if MASSCAN_CUSTOM_PORTS else f"top-{MASSCAN_TOP_PORTS}",
                "rate_limit": MASSCAN_RATE,
                "banners_enabled": MASSCAN_BANNERS,
                "total_targets": len(all_targets),
            },
            "by_host": results["by_host"],
            "by_ip": results["by_ip"],
            "all_ports": results["all_ports"],
            "ip_to_hostnames": ip_to_hostnames,
            "summary": results["summary"],
        }

        summary = results["summary"]
        print(f"[✓][Masscan] Scan completed in {duration:.1f} seconds")
        print(f"[+][Masscan] Hosts with open ports: {summary['hosts_with_open_ports']}")
        print(f"[+][Masscan] Total open ports found: {summary['total_open_ports']}")
        print(f"[+][Masscan] Unique ports: {summary['unique_port_count']}")

        if results["all_ports"]:
            ports_preview = ', '.join(map(str, results['all_ports'][:20]))
            extra = f"... (+{len(results['all_ports'])-20} more)" if len(results['all_ports']) > 20 else ""
            print(f"[+][Masscan] Ports discovered: {ports_preview}{extra}")

        recon_data["masscan_scan"] = masscan_results

        if output_file:
            with open(output_file, 'w') as f:
                json.dump(recon_data, f, indent=2, default=str)
            print(f"[✓][Masscan] Results saved to {output_file}")

        return recon_data

    except subprocess.TimeoutExpired:
        print("[!][Masscan] Scan timed out after 30 minutes — killing process")
        try:
            process.kill()
            process.wait(timeout=10)
        except Exception:
            pass
        return recon_data
    except Exception as e:
        print(f"[!][Masscan] Error during scan: {e}")
        return recon_data
    finally:
        try:
            if scan_temp_dir.exists():
                for f in scan_temp_dir.iterdir():
                    f.unlink()
                scan_temp_dir.rmdir()
        except Exception:
            pass


def run_masscan_scan_isolated(recon_data: dict, settings: dict = None) -> dict:
    """
    Run masscan scan and return only the 'masscan_scan' data dict.

    Thread-safe: does not mutate recon_data.

    Args:
        recon_data: The pipeline's combined result dictionary (read-only)
        settings: Settings dictionary from main.py

    Returns:
        The 'masscan_scan' data dict, or empty dict if scan produced no results.
    """
    import copy
    snapshot = copy.copy(recon_data)
    run_masscan_scan(snapshot, output_file=None, settings=settings)
    return snapshot.get("masscan_scan", {})
