def build_masscan_command(targets_file: str, output_file: str, settings: dict) -> List[str]:
    """
    Build the masscan command with project settings.

    Args:
        targets_file: Path to file with one IP/CIDR per line
        output_file: Path for NDJSON output (one JSON object per line)
        settings: Settings dictionary from main.py

    Returns:
        List of command arguments
    """
    MASSCAN_CUSTOM_PORTS = settings.get('MASSCAN_CUSTOM_PORTS', '')
    MASSCAN_TOP_PORTS = settings.get('MASSCAN_TOP_PORTS', '1000')
    MASSCAN_RATE = settings.get('MASSCAN_RATE', 1000)
    MASSCAN_BANNERS = settings.get('MASSCAN_BANNERS', False)
    MASSCAN_WAIT = settings.get('MASSCAN_WAIT', 10)
    MASSCAN_RETRIES = settings.get('MASSCAN_RETRIES', 1)
    MASSCAN_EXCLUDE_TARGETS = settings.get('MASSCAN_EXCLUDE_TARGETS', '')

    cmd = ["masscan"]

    # Targets from file
    cmd.extend(["-iL", targets_file])

    # Port configuration
    if MASSCAN_CUSTOM_PORTS:
        cmd.extend(["-p", MASSCAN_CUSTOM_PORTS])
    elif MASSCAN_TOP_PORTS:
        top_ports_str = str(MASSCAN_TOP_PORTS)
        if top_ports_str.lower() == 'full':
            cmd.extend(["-p", "0-65535"])
        else:
            cmd.extend(["--top-ports", top_ports_str])

    # Performance
    cmd.extend(["--rate", str(MASSCAN_RATE)])
    cmd.extend(["--wait", str(MASSCAN_WAIT)])
    cmd.extend(["--retries", str(MASSCAN_RETRIES)])

    # Banner grabbing
    if MASSCAN_BANNERS:
        cmd.append("--banners")

    # Output: NDJSON (one JSON object per line, avoids -oJ trailing comma bug)
    cmd.extend(["-oD", output_file])

    # Exclude targets
    if MASSCAN_EXCLUDE_TARGETS.strip():
        exclude_file = str(Path(output_file).parent / "masscan_exclude.txt")
        with open(exclude_file, 'w') as f:
            for line in MASSCAN_EXCLUDE_TARGETS.strip().split(','):
                line = line.strip()
                if line:
                    f.write(f"{line}\n")
        cmd.extend(["--excludefile", exclude_file])

    return cmd


