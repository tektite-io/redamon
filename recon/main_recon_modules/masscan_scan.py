def resolve_targets_to_ips(recon_data: dict) -> Tuple[List[str], Dict[str, List[str]]]:
    """
    Extract IPs from recon data. Masscan requires IP addresses (not hostnames).

    Returns:
        Tuple of (ip_list, ip_to_hostnames mapping)
    """
    unique_ips: Set[str] = set()
    ip_to_hostnames: Dict[str, List[str]] = {}

    dns_data = recon_data.get("dns", {})

    # Root domain
    domain_dns = dns_data.get("domain", {})
    domain_name = recon_data.get("domain", "")
    if domain_name:
        for ip in domain_dns.get("ips", {}).get("ipv4", []) + domain_dns.get("ips", {}).get("ipv6", []):
            unique_ips.add(ip)
            ip_to_hostnames.setdefault(ip, [])
            if domain_name not in ip_to_hostnames[ip]:
                ip_to_hostnames[ip].append(domain_name)

    # Subdomains
    for subdomain, sub_data in dns_data.get("subdomains", {}).items():
        if not sub_data.get("has_records", False):
            continue
        for ip in sub_data.get("ips", {}).get("ipv4", []) + sub_data.get("ips", {}).get("ipv6", []):
            unique_ips.add(ip)
            ip_to_hostnames.setdefault(ip, [])
            if subdomain not in ip_to_hostnames[ip]:
                ip_to_hostnames[ip].append(subdomain)

    return list(unique_ips), ip_to_hostnames


