# Result Parsing
# =============================================================================

def parse_masscan_output(output_file: str, ip_to_hostnames: Dict[str, List[str]]) -> Dict:
    """
    Parse Masscan NDJSON (-oD) output into the same structure as Naabu's parser.

    Masscan NDJSON format — one record per line, one port per record:
    {"ip":"x.x.x.x","timestamp":"...","port":80,"proto":"tcp","rec_type":"status","data":{"status":"open","reason":"syn-ack","ttl":48}}

    With --banners, banner records also appear:
    {"ip":"x.x.x.x","timestamp":"...","port":80,"proto":"tcp","rec_type":"banner","data":{"banner":"..."}}

    Note: this is NOT the -oJ format which uses ports:[{...}] arrays.

    Returns:
        Dict with by_host, by_ip, all_ports, summary — identical to Naabu's output
    """
    by_host: Dict = {}
    by_ip: Dict = {}
    all_ports: Set[int] = set()

    if not Path(output_file).exists():
        return _empty_result()

    with open(output_file, 'r') as f:
        for line in f:
            line = line.strip()
            if not line or line.startswith('#'):
                continue

            try:
                entry = json.loads(line)
            except json.JSONDecodeError:
                continue

            ip = entry.get("ip", "")
            if not ip:
                continue

            # NDJSON has port/proto at top level (not in a "ports" array)
            port = entry.get("port")
            proto = entry.get("proto", "tcp")
            rec_type = entry.get("rec_type", "")
            data = entry.get("data", {})

            # Only process status records with open ports; skip banner records
            if rec_type == "banner":
                continue
            status = data.get("status", "") if isinstance(data, dict) else ""
            if port is None or status != "open":
                continue

            hostnames = ip_to_hostnames.get(ip, [])

            # Initialize IP record
            if ip not in by_ip:
                by_ip[ip] = {
                    "ip": ip,
                    "hostnames": list(hostnames),
                    "ports": [],
                    "cdn": None,
                    "is_cdn": False,
                }

            all_ports.add(port)

            # Add to by_ip
            if port not in by_ip[ip]["ports"]:
                by_ip[ip]["ports"].append(port)

            # Add to by_host for each hostname mapped to this IP
            for hostname in hostnames:
                if hostname not in by_host:
                    by_host[hostname] = {
                        "host": hostname,
                        "ip": ip,
                        "ports": [],
                        "port_details": [],
                        "cdn": None,
                        "is_cdn": False,
                    }
                if port not in by_host[hostname]["ports"]:
                    by_host[hostname]["ports"].append(port)
                    service = get_service_name(port)
                    by_host[hostname]["port_details"].append({
                        "port": port,
                        "protocol": proto,
                        "service": service,
                    })

            # If no hostname mapping, use the IP itself as the host key
            if not hostnames:
                if ip not in by_host:
                    by_host[ip] = {
                        "host": ip,
                        "ip": ip,
                        "ports": [],
                        "port_details": [],
                        "cdn": None,
                        "is_cdn": False,
                    }
                if port not in by_host[ip]["ports"]:
                    by_host[ip]["ports"].append(port)
                    service = get_service_name(port)
                    by_host[ip]["port_details"].append({
                        "port": port,
                        "protocol": proto,
                        "service": service,
                    })

    # Sort ports
    for host_data in by_host.values():
        host_data["ports"].sort()
        host_data["port_details"].sort(key=lambda x: x["port"])
    for ip_data in by_ip.values():
        ip_data["ports"].sort()

    all_ports_sorted = sorted(list(all_ports))

    summary = {
        "hosts_scanned": len(by_host),
        "ips_scanned": len(by_ip),
        "hosts_with_open_ports": len([h for h in by_host.values() if h["ports"]]),
        "total_open_ports": sum(len(h["ports"]) for h in by_host.values()),
        "unique_ports": all_ports_sorted,
        "unique_port_count": len(all_ports_sorted),
        "cdn_hosts": 0,
    }

    return {
        "by_host": by_host,
        "by_ip": by_ip,
        "all_ports": all_ports_sorted,
        "summary": summary,
    }


def _empty_result() -> Dict:
    return {
        "by_host": {},
        "by_ip": {},
        "all_ports": [],
        "summary": {
            "hosts_scanned": 0,
            "ips_scanned": 0,
            "hosts_with_open_ports": 0,
            "total_open_ports": 0,
            "unique_ports": [],
            "unique_port_count": 0,
            "cdn_hosts": 0,
        },
    }


